<li>Updated the build to use <code>-gnat2012</code> in all gpr files.</li><li>Switched from <code>AUnit</code> to <code>Ahven</code> for unit testing.</li><li>Minor source code changes based on stricter checks with GNAT 2016.</li></ul><br /><a href="http://zanyblue.sourceforge.net">http://zanyblue.sourceforge.net</a><br /><br />From Michael Rohan.<br /><br /><span class="blog-read-more"><a href="7786bb14e48da1ba150867ee0aa23470-26.php"> Read More…</a></span><p class="blog-entry-tags">Tags: <a href="tag-localization.php" title="localization" rel="tag">localization</a>,<a href="tag-l10n.php" title="l10n" rel="tag">l10n</a>,<a href="tag-globalization.php" title="Globalization" rel="tag">Globalization</a>,<a href="tag-g11n.php" title="g11n" rel="tag">g11n</a></p></div></div>
